    public void initNoticeboard()
    {
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DBCon.connectDB();
        
            String sql = "Select * from noticeboard where projid = ? order by date desc, msg_id desc";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, pID);
            
            ResultSet rs = pst.executeQuery();
            
            while (rs.next())
            {
                message t = new message(this,rs.getInt("msg_id"),pID);
                t.setValues(rs.getString("username"),rs.getString("date"),rs.getString("text"), rs.getString("title"));
                ms.add(t);
                jPanel3.add(t);
            }
            
            con.close();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
    }
            
        
    
    
