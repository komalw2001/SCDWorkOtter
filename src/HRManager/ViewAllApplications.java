    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JTextField name = new JTextField();
        JTextField contact = new JTextField();
        JTextField degree = new JTextField();
        JTextField institution = new JTextField();
        JTextField cgpa = new JTextField();
        JTextField experience = new JTextField();
        JTextField skills = new JTextField();
        
        Object[] fields = {
            "Applicant Name:", name, 
            "Contact:", contact,
            "Recent Degree", degree,
            "Recent Institution", institution,
            "Graduating CGPA", cgpa,
            "Experience in Years", experience,
            "Skills", skills
        };
               
        int option = JOptionPane.showConfirmDialog(null, fields, "Applicant Details", JOptionPane.OK_CANCEL_OPTION);
        
        
        if (option == JOptionPane.OK_OPTION)
        {
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           
           String uname = name.getText();
            String pw = contact.getText();
            String nm = degree.getText();
            String des = institution.getText();
            String sal = cgpa.getText();
           String exp = experience.getText();
           String skill = skills.getText();
            if(uname.equals("") || pw.equals("") || des.equals("") || sal.equals("") || exp.equals("") || skill.equals(""))
            {
                String msg = "One or more fields empty. try again.";
                JOptionPane.showMessageDialog(null, msg);
                return;
            }
           model.addRow(new Object[]{uname, pw, nm,des,sal,exp,skill});
           
           String query = "Insert into application(name,contact,degree,institution, cgpa, experience,skills) values(?,?,?,?,?,?,?)";
            try {
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1,uname);
                pst.setString(2,pw);
                pst.setString(3,nm);
                pst.setString(4,des);
                pst.setString(5,sal);
                pst.setString(6,exp);
                pst.setString(7,skill);
                
                pst.execute();
                
                LoadTable();
                
            } 
             catch(SQLIntegrityConstraintViolationException exc)
            {
                System.out.println("Primary ket exception adding new applicant");
                exc.printStackTrace();
            }
            catch (SQLException ex) {
                
                System.out.println("Exception while adding new applicant");
                ex.printStackTrace();
//Logger.getLogger(ManageEmployees.class.getName()).log(Level.SEVERE, null, ex);
            }

            
        } 
        
    }//GEN-LAST:event_jButton2ActionPerformed
