        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        
       
        if (row == -1)
        {
            String err = "Select a row to shortlist";
            JOptionPane.showMessageDialog(null, err);
        }
        else
            System.out.println("rows = " + row); 
        if (row!=-1) //row is -1 if nothing selected
        {
            Object username = dtm.getValueAt(row, 0);
            System.out.println("shortlisting = " + username); //yeh basically id hai
            
            try {
                PreparedStatement pst = con.prepareStatement("Select name,contact from application where id = ?");
                pst.setString(1,(String) username);
               ResultSet rs = pst.executeQuery();
               
               String name = null;
               String contact = null;
            while(rs.next()){
               System.out.println("SHORTLIST");
               name = rs.getString("name");
            contact = rs.getString("contact");
            }
            
            JTextField designation = new JTextField();
            JTextField hiringstatus = new JTextField();
            Object[] msg = {"Designation ",designation, "Hiring Status",hiringstatus };
            int option = JOptionPane.showConfirmDialog(null, msg, "Shortlisting Details", JOptionPane.OK_CANCEL_OPTION);
         if(designation.getText().equals("") || hiringstatus.getText().equals(""))
            {
                String msg1 = "One or more fields empty. try again.";
                JOptionPane.showMessageDialog(null, msg1);
                return;
            }
         
         if (!designation.getText().equals("Developer") && !designation.getText().equals("Project Manager"))
             {
                 JOptionPane.showMessageDialog(null, "Invalid designation!");
                return;
             }
         
            if (option == JOptionPane.OK_OPTION)
            {
                PreparedStatement p = con.prepareStatement("Insert into shortlisted(name,contact,designation,hiringstatus) values (?,?,?,?)");
                p.setString(1,name);
                p.setString(2,contact);
                
                String des = designation.getText();
                String hs = hiringstatus.getText();
                p.setString(3,des);
                p.setString(4,hs);
                p.execute();
            }
                   
                

            } 
            catch (SQLException ex) {
               
                System.out.println("\nDeletion exception");
                ex.printStackTrace();
            }
        }
        
